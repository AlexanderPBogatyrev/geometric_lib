import math

def area(r):
    '''
    Принимает число r, возвращает площадь круга.
        Параметры:
            r (int): радиус окружности в формате десятичного числа
        Возращаемое значение:
            Площадь окружности
        Пример:
            area(4) -> ~(3 * 4 * 4) ~= 27 
    ''' 
    if r <= 0: return 0
    return math.pi * r * r

def perimeter(r):
    '''
    Принимает число r, возвращает длину окружности.
        Параметры:
            r (int): радиус окружности в формате десятичного числа
        Возращаемое значение:
            Длина окружности
        Пример:
            area(4) -> ~(2 * 3 * 4) ~= 24 
    ''' 
    if r <= 0: return 0
    return 2 * math.pi * r
